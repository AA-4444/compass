export interface QuizResult {
  answers: number[];
  total: number;
  average: number;
}

export function calculateResult(answers: number[]): QuizResult {
  const total = answers.reduce((sum, a) => sum + a, 0);
  return {
    answers,
    total,
    average: total / answers.length,
  };
}

